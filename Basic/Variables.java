public class Variables {
    public static void main(String[] args) {
        
        int x = 5;
        System.out.println(x);

        int y; //intialization
        y = 20; //declarations
        System.out.println(y);
        
        int z = x * y;
        System.out.println(z);

        x = 10;
        System.out.println(x);

        x = x + 5;
        System.out.println(x);

        x = x - 2;
        System.out.println(x);

        x = x * 2;
        System.out.println(x);

        x = x / 2;
        System.out.println(x);

    }
}